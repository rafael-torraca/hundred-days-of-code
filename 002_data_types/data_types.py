# Subscripting

print("Hello"[0])
print("Hello"[-1])
print("Hello"[-3])

# String

print("123" + "345")
print("123", "345")
print("123" * 3)

# Integer

print(123 + 345)
print(123_456 + 654_321)


# Float

print(3.14 * 2)

# Boolean

print(True)
print(False)
print(True and False)
print(True or False)
print(not True)
print(not False)


print(type("Hello"))
print(type(123))
print(type(1.23))
print(type(True))
print(type(range(10)))